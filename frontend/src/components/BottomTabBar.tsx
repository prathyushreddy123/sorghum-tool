export default function BottomTabBar() {
  const location = useLocation();
  const navigate = useNavigate();

  // Extract trialId from current path for dashboard tab
  const trialMatch = location.pathname.match(/\/trials\/(\d+)/);
  const currentTrialId = trialMatch ? trialMatch[1] : null;

  function isActive(key: string): boolean {
    switch (key) {
      case 'trials':
        return location.pathname === '/' || location.pathname === '/trials/new';
      case 'dashboard':
        return !!(trialMatch && !location.pathname.includes('/collect') && location.pathname !== '/settings');
      case 'settings':
        return location.pathname === '/settings';
      default:
        return false;
    }
  }

  function handleTap(key: string) {
    switch (key) {
      case 'trials':
        navigate('/');
        break;
      case 'dashboard':
        if (currentTrialId) {
          navigate(`/trials/${currentTrialId}`);
        } else {
          navigate('/');
        }
        break;
      case 'settings':
        navigate('/settings');
        break;
    }
  }

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-40 bg-white border-t border-gray-200 shadow-[0_-2px_10px_rgba(0,0,0,0.08)]">
      <div className="max-w-2xl mx-auto flex">
        {TABS.map(({ key, label, icon }) => {
          const active = isActive(key);
          return (
            <button
              key={key}
              onClick={() => handleTap(key)}
              className={`flex-1 flex flex-col items-center justify-center py-2 min-h-[56px] transition-colors ${
                active ? 'text-primary' : 'text-gray-400'
              }`}
            >
              {icon}
              <span className="text-xs mt-0.5 font-medium">{label}</span>
            </button>
          );
        })}
      </div>
    </nav>
  );
}
